import Foundation

/// Supported target languages for live translation.
enum LanguageOption: String, CaseIterable {
    case english = "English"
    case japanese = "Japanese"
    case chinese = "Chinese"
    case korean = "Korean"
    case spanish = "Spanish"
    case french = "French"
    case german = "German"
}
